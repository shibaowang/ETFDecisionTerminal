def changed_paths(root: Path) -> set[str]:
    paths: set[str] = set()
    commands = [
        ["git", "diff", "--name-only", "main"],
        ["git", "diff", "--cached", "--name-only"],
        ["git", "ls-files", "--others", "--exclude-standard"],
    ]
    for command in commands:
        completed = subprocess.run(
            command,
            cwd=root,
            check=True,
            capture_output=True,
            text=True,
        )
        paths.update(line.strip().replace("\\", "/") for line in completed.stdout.splitlines() if line.strip())

    status = subprocess.run(
        ["git", "status", "--porcelain=v1", "-uall"],
        cwd=root,
        check=True,
        capture_output=True,
        text=True,
    )
    for line in status.stdout.splitlines():
        if not line:
            continue
        path = line[3:].strip().replace("\\", "/")
        if " -> " in path:
            path = path.split(" -> ", 1)[1]
        if path:
            paths.add(path)

    return {path for path in paths if path not in EPIC_277_ALLOWED_CHANGED_PATHS}

def registered_ctests(root: Path) -> set[str]:
    completed = run(["ctest", "--test-dir", str(root / "build"), "-N"], root)
    names: set[str] = set()
    for line in completed.stdout.splitlines():
        match = re.search(r"Test\s+#\d+:\s+(\S+)", line)
        if match:
            names.add(match.group(1))
    return names


def text_without_policy_constant_tables(text: str) -> str:
    kept: list[str] = []
    skipping = False
    for line in text.splitlines():
        if re.match(r"^[A-Z0-9_]*(BAD|FORBIDDEN|KNOWN)[A-Z0-9_]*\s*=\s*[\(\[]", line):
            skipping = True
            continue
        if skipping:
            if line.strip() in {")", "]"}:
                skipping = False
            continue
        kept.append(line)
    return "\n".join(kept)


def text_without_static_guard_lines(text: str) -> str:
    kept: list[str] = []
    for line in text_without_policy_constant_tables(text).splitlines():
        stripped = line.strip()
        if any(
            marker in stripped
            for marker in (
                "gate.require(",
                ".require(",
                "for token in",
                "if token",
                "not in text",
                "not in source_scan_text",
                "not in executable_scan_text",
            )
        ):
            continue
        kept.append(line)
    return "\n".join(kept)


