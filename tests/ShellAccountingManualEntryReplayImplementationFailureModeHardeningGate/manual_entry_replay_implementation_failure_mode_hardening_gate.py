def validate_failure_output(gate: Gate, label: str, completed: subprocess.CompletedProcess[str]) -> None:
    gate.require(completed.returncode != 0, f"failure case returns non-zero: {label}")
    combined = (completed.stdout + completed.stderr).lower()
    for token in FORBIDDEN_OUTPUT_TOKENS:
        gate.require(token not in combined, f"failure output excludes `{token}` for {label}")
    gate.require("{" in completed.stdout, f"failure stdout contains JSON object: {label}")
    failure = json.loads(completed.stdout)
    gate.require(isinstance(failure, dict), f"failure summary object: {label}")
    gate.require(failure.get("schemaVersion") == FAILURE_SCHEMA, f"failure schema correct: {label}")
    gate.require(failure.get("ok") is False, f"failure ok false: {label}")
    gate.require(failure.get("failureCount") == 1, f"failure count one: {label}")
    failures = failure.get("failures")
    gate.require(isinstance(failures, list) and len(failures) == 1, f"failure list shape: {label}")
    item = failures[0]
    gate.require(isinstance(item, dict), f"failure item object: {label}")
    gate.require(item.get("issueCode") == "REPLAY_IMPLEMENTATION_CONTRACT_VIOLATION", f"issue code stable: {label}")
    gate.require(item.get("rule") == "dry-run-summary-contract", f"failure rule stable: {label}")
    gate.require(item.get("file") == "dry-run-summary", f"failure file sanitized: {label}")
    gate.require(isinstance(item.get("message"), str) and "sanitized" in item["message"], f"message sanitized: {label}")
    encoded = json.dumps(failure, sort_keys=True).lower()
    for token in FORBIDDEN_OUTPUT_TOKENS:
        gate.require(token not in encoded, f"failure JSON excludes `{token}` for {label}")


def run_failure_cases(gate: Gate, root: Path, baseline: dict[str, Any]) -> None:
    with tempfile.TemporaryDirectory(prefix="task236_replay_failure_") as tmp:
        tmp_dir = Path(tmp)
        baseline_path = tmp_dir / "baseline_summary.json"
        baseline_path.write_text(json.dumps(baseline, sort_keys=True), encoding="utf-8")
        gate.require(baseline_path.exists(), "temporary baseline summary exists")

        for index, (label, mutator) in enumerate(failure_cases(), start=1):
            bad_summary = clone_summary(baseline)
            payload = mutator(bad_summary)
            input_path = tmp_dir / f"bad_summary_{index:02d}.json"
            write_bad_summary(input_path, payload)
            gate.require(input_path.exists(), f"temporary bad summary exists: {label}")
            completed = subprocess.run(
                [
                    sys.executable,
                    str(root / TASK_234_IMPLEMENTATION),
                    "--source-root",
                    str(root),
                    "--summary-json",
                    "--dry-run-summary-json-file",
                    str(input_path),
                ],
                cwd=root,
                check=False,
                capture_output=True,
                text=True,
            )
            validate_failure_output(gate, label, completed)


