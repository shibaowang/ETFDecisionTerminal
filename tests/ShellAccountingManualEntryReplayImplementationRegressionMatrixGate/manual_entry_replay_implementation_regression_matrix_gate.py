def run_json_script(gate: Gate, root: Path, script: Path, schema: str) -> tuple[str, dict[str, Any]]:
    completed = subprocess.run(
        [sys.executable, str(root / script), "--source-root", str(root), "--summary-json"],
        cwd=root,
        check=False,
        capture_output=True,
        text=True,
    )
    gate.require(completed.returncode == 0, f"{script.as_posix()} exits zero")
    gate.require(completed.stderr.strip() == "", f"{script.as_posix()} stderr is empty")
    stdout = completed.stdout.strip()
    gate.require(stdout.startswith("{") and stdout.endswith("}"), f"{script.as_posix()} stdout is one JSON object")
    summary = json.loads(stdout)
    gate.require(isinstance(summary, dict), f"{script.as_posix()} summary object")
    gate.require(summary.get("schemaVersion") == schema, f"{script.as_posix()} schema is exact")
    return stdout, summary


