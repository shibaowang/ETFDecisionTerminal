def validate_static_boundaries(gate: Gate, root: Path) -> None:
    runtime_text = repo_text(root, TASK_RUNTIME)
    gate.contains(runtime_text, "AccountingReplayEngine", "runtime helper uses AccountingReplayEngine")
    gate.contains(runtime_text, "replayReadOnly", "runtime helper calls read-only replay")
    gate.contains(runtime_text, "productionWrite", "runtime evidence reports productionWrite")
    gate.contains(runtime_text, "sqliteProductionWrite", "runtime evidence reports sqliteProductionWrite")

    lowered = runtime_text.lower()
    for token in ("sqlite3", "insert into", "update ", "delete from", "replace into", "drop table"):
        gate.require(token not in lowered, f"runtime helper has no SQL token {token}")
    for token in ("broker", "endpoint", "credential", "network"):
        # The helper may emit false evidence flag names for these boundaries, but must not contain active APIs.
        gate.require(f"import {token}" not in lowered, f"runtime helper imports no {token}")

    for path in (
        Path("apps/ETFDecisionShell"),
        Path("libs/DataServiceApi"),
        Path("libs/DataAccess"),
        Path("migrations"),
    ):
        gate.require(not any(changed.startswith(path.as_posix() + "/") and changed not in TASK_257_EXACT_PATHS and changed not in TASK_270_EXACT_PATHS and changed not in TASK_271_EXACT_PATHS for changed in changed_paths(root)),
                     f"{path} unchanged")

    for production_path in (
        Path("apps/ETFDecisionShell/qml"),
        Path("apps/ETFDecisionShell/src/main.cpp"),
        Path("libs/ShellServices/src"),
        Path("libs/ShellServices/include"),
    ):
        if not (root / production_path).exists():
            continue
        for file_path in (root / production_path).rglob("*"):
            if file_path.is_file() and file_path.suffix in {".qml", ".cpp", ".h", ".hpp"}:
                text = file_path.read_text(encoding="utf-8", errors="ignore")
                gate.require("AccountingReplayEngine" not in text, f"{production_path} does not directly construct AccountingReplayEngine")
                gate.require("replayReadOnly" not in text, f"{production_path} does not directly call replayReadOnly")


def run_runtime_helper(gate: Gate, runtime_executable: Path, root: Path) -> dict[str, Any]:
    completed = subprocess.run(
        [str(runtime_executable)],
        cwd=root,
        check=False,
        capture_output=True,
        text=True,
    )
    gate.require(completed.returncode == 0, f"runtime helper exits 0: {completed.stderr}")
    gate.require(completed.stderr.strip() == "", "runtime helper stderr is empty")
    stdout = completed.stdout.strip()
    gate.require(stdout.startswith("{") and stdout.endswith("}"), "runtime helper emits JSON object")
    lowered = stdout.lower()
    for token in FORBIDDEN_OUTPUT_TOKENS:
        gate.require(token not in lowered, f"evidence output omits forbidden token {token}")
    value = json.loads(stdout)
    gate.require(isinstance(value, dict), "evidence JSON is object")
    return value


def validate_evidence(gate: Gate, evidence: dict[str, Any]) -> None:
    gate.require(evidence.get("task") == "TASK-252", "evidence task is TASK-252")
    gate.require(evidence.get("mode") == "test-only-readonly-runtime-integration-vertical-slice",
                 "evidence mode is test-only vertical slice")
    gate.require(evidence.get("runtimeIntegrationPath") == "test-only", "runtimeIntegrationPath is test-only")
    for flag in EXPECTED_TRUE_FLAGS:
        gate.require(evidence.get(flag) is True, f"{flag}=true")
    for flag in EXPECTED_FALSE_FLAGS:
        gate.require(evidence.get(flag) is False, f"{flag}=false")

    summary = evidence.get("projectionSummary")
    gate.require(isinstance(summary, dict), "projectionSummary is object")
    gate.require(summary.get("instrumentCode") == "TASK252_INSTRUMENT", "projection instrumentCode is sanitized")
    gate.require(summary.get("quantity") == "100", "projection quantity is stable")
    gate.require(summary.get("cashBalance") == "99799.00 CNY", "projection cash balance is stable")
    gate.require(summary.get("pnlStatus") == "UNAVAILABLE", "projection PnL status is safe unavailable")

    failure_modes = evidence.get("failureModes")
    gate.require(isinstance(failure_modes, dict), "failureModes is object")
    for key in (
        "missingInput",
        "malformedInput",
        "unsupportedScenario",
        "replayIssueReturned",
        "sanitizedErrorOutput",
        "noWriteOnFailure",
    ):
        gate.require(failure_modes.get(key) is True, f"failure mode covered: {key}")

    privacy = evidence.get("privacy")
    gate.require(isinstance(privacy, dict), "privacy is object")
    for key in ("rawSqlExposed", "rawTradeLogPayloadExposed", "rawReplayPayloadExposed", "internalStackExposed"):
        gate.require(privacy.get(key) is False, f"privacy {key}=false")
    gate.require(privacy.get("sanitizedFailureExampleSafe") is True, "sanitized failure example is safe")


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--source-root", required=True)
    parser.add_argument("--runtime-executable", required=True)
    args = parser.parse_args()

    root = Path(args.source_root)
    runtime_executable = Path(args.runtime_executable)
    gate = Gate()

    validate_changed_paths(gate, root)
    validate_docs(gate, root)
    validate_static_boundaries(gate, root)
    evidence = run_runtime_helper(gate, runtime_executable, root)
    validate_evidence(gate, evidence)
    gate.require(gate.count >= 90, f"expected at least 90 checks, got {gate.count}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
