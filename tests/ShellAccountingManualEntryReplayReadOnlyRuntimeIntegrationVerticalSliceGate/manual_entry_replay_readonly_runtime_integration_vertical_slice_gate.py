class Gate:
    def __init__(self) -> None:
        self.count = 0

    def require(self, condition: bool, message: str) -> None:
        self.count += 1
        if not condition:
            raise AssertionError(message)

    def contains(self, text: str, needle: str, message: str) -> None:
        self.require(needle in text, message)


def repo_text(root: Path, path: Path) -> str:
    return (root / path).read_text(encoding="utf-8")


def changed_paths(root: Path) -> set[str]:
    completed = subprocess.run(
        ["git", "status", "--porcelain=v1", "-uall"],
        cwd=root,
        check=True,
        capture_output=True,
        text=True,
    )
    paths: set[str] = set()
    for line in completed.stdout.splitlines():
        if len(line) < 4:
            continue
        value = line[3:]
        if " -> " in value:
            value = value.split(" -> ", 1)[1]
        paths.add(value.replace("\\", "/"))
    return paths


def validate_changed_paths(gate: Gate, root: Path) -> None:
    changes = changed_paths(root)
    unexpected = sorted(path for path in changes if path not in ALLOWED_CHANGED_PATHS)
    gate.require(not unexpected, f"unexpected changed paths: {unexpected}")
    for path in changes:
        if path in TASK_257_EXACT_PATHS:
            continue
        gate.require(not path.startswith(FORBIDDEN_CHANGED_PREFIXES), f"forbidden changed path: {path}")
    for required in (TASK_DOC, TASK_PLAN, TASK_CMAKE, TASK_GATE, TASK_RUNTIME):
        gate.require(required.as_posix() in ALLOWED_CHANGED_PATHS, f"required path allowlisted: {required}")
    gate.require(all(not path.endswith("/") for path in ALLOWED_CHANGED_PATHS), "allowlist uses exact file paths")
    gate.require(all("*" not in path for path in ALLOWED_CHANGED_PATHS), "allowlist has no wildcards")


