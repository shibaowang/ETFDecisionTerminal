def changed_paths(root: Path) -> set[str]:
    paths: set[str] = set()
    commands = [
        ["git", "diff", "--name-only", "main"],
        ["git", "diff", "--cached", "--name-only"],
        ["git", "ls-files", "--others", "--exclude-standard"],
    ]
    for command in commands:
        completed = subprocess.run(
            command,
            cwd=root,
            check=True,
            capture_output=True,
            text=True,
        )
        paths.update(line.strip().replace("\\", "/") for line in completed.stdout.splitlines() if line.strip())

    status = subprocess.run(
        ["git", "status", "--porcelain=v1", "-uall"],
        cwd=root,
        check=True,
        capture_output=True,
        text=True,
    )
    for line in status.stdout.splitlines():
        if not line:
            continue
        path = line[3:].strip().replace("\\", "/")
        if " -> " in path:
            path = path.split(" -> ", 1)[1]
        if path:
            paths.add(path)

    return {path for path in paths if path not in EPIC_277_ALLOWED_CHANGED_PATHS}

def ctest_names(root: Path, gate: Gate) -> set[str]:
    build = root / "build"
    gate.require(build.exists(), "build directory exists for CTest registration probe")
    completed = subprocess.run(
        ["ctest", "--test-dir", str(build), "-N"],
        cwd=root,
        check=False,
        capture_output=True,
        text=True,
    )
    gate.require(completed.returncode == 0, "ctest -N exits zero")
    names: set[str] = set()
    for line in completed.stdout.splitlines():
        match = re.search(r"Test\s+#\d+:\s+(\S+)", line)
        if match:
            names.add(match.group(1))
    gate.require(bool(names), "ctest -N returns registered tests")
    return names


